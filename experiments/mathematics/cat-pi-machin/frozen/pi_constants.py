# Algorithm: Machin's formula (1706)
# pi/4 = 4*arctan(1/5) - arctan(1/239)
# arctan(x) = x - x^3/3 + x^5/5 - x^7/7 + ...
# This converges much faster than Leibniz (which needs millions of terms)
REQUIRED_ALGORITHM = "machin_formula"

# LLM prior errors for pi:
# 1. Leibniz series: pi/4 = 1 - 1/3 + 1/5 - 1/7 + ...
#    Needs ~10^N terms for N digits. Useless for large N.
# 2. Monte Carlo: random darts in unit square
#    Needs ~10^(2N) samples for N digits. Also useless.
# 3. math.pi: float precision only (15 digits)
# All three are PROHIBITED.

PROHIBITED = ["math.pi", "mpmath", "sympy", "monte", "carlo",
              "random", "uniform", "leibniz", "numpy.pi"]

TARGET_DIGITS = 10000
